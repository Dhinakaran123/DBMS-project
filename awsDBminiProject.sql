  CREATE OR REPLACE EDITIONABLE PROCEDURE "DK_ANNANUNIV"."UPDATE_JOB_STATUS" AS
BEGIN
    FOR i IN (SELECT * FROM jobs)
    LOOP
        IF SYSDATE >= i.dateofopen AND SYSDATE <= i.dateofclose AND i.status = 'inactive' THEN
            UPDATE jobs SET status = 'active' WHERE jid = i.jid;
        ELSIF SYSDATE > i.dateofclose AND i.status = 'active' THEN
            UPDATE jobs SET status = 'inactive' WHERE jid = i.jid;
        END IF;
    END LOOP;
    COMMIT;
END;

