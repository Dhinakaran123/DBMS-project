  SELECT a.aid,a.jid,j.jobname,(SELECT c.cmpname FROM company c WHERE j.cid = c.cid) AS cmpname,
  (SELECT c.phone FROM company c WHERE j.cid = c.cid) AS phone,
  a.appliedon,a.userstatus,a.selection_status,a.viewed
  FROM jobApply a INNER JOIN jobs j ON a.jid = j.jid WHERE a.id = '1' ORDER BY a.appliedon DESC
;
